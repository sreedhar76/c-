#include <iostream>

int main() {
    int integerNumber = 10;
    std::cout << "Integer number: " << integerNumber << std::endl;

    float floatNumber = 3.14f;
    std::cout << "Float number: " << floatNumber << std::endl;


    char charValue = 'A';
    std::cout << "Character value: " << charValue << std::endl;

    return 0;
}

